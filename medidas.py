# Programa completo de conversão de medidas

# Primeiro, o tradicional "Olá Mundo"
print("Olá Mundo!")

# Valor em metros já definido
metros = 5.0  # você pode mudar esse valor

# Conversões
centimetros = metros * 100
milimetros = metros * 1000
quilometros = metros / 1000

# Exibe os resultados
print(f"Valor em metros: {metros:.2f} m")
print(f"Em centímetros: {centimetros:.2f} cm")
print(f"Em milímetros: {milimetros:.2f} mm")
print(f"Em quilômetros: {quilometros:.3f} km")