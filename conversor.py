# Programa completo de conversão de dólar para real

# Primeiro, o tradicional "Olá Mundo"
print("Olá Mundo!")

# Cotação fixa do dólar
cotacao_dolar = 6.00

# Valor em dólares já definido
valor_dolar = 10.00  # você pode mudar esse valor

# Faz a conversão
valor_real = valor_dolar * cotacao_dolar

# Exibe o resultado
print(f"{valor_dolar:.2f} dólares equivalem a R$ {valor_real:.2f}")
